// 获取每日成语（根据日期）
export function getDailyIdiom(): string {
  const today = new Date();
  const dateString = `${today.getFullYear()}${today.getMonth()}${today.getDate()}`;
  const index = parseInt(dateString, 10) % IDIOMS.length;
  return IDIOMS[index];
}

// 随机获取一个成语
export function getRandomIdiom(): string {
  return IDIOMS[Math.floor(Math.random() * IDIOMS.length)];
}

// 检查是否是有效的四字词语
export function isValidIdiom(input: string): boolean {
  if (!input || typeof input !== 'string') return false;
  if (input.length !== 4) return false;
  // 只检查是否是四个汉字，不限制是否在词库中
  return /^[\u4e00-\u9fa5]{4}$/.test(input);
}
